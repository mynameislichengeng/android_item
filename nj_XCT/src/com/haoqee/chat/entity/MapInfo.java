package com.haoqee.chat.entity;

import java.io.Serializable;

public class MapInfo implements Serializable{
	private static final long serialVersionUID = -1153484834874L;
	private String ctiy;
	private String addr;
	private String lat;
	private String lon;

	public MapInfo(String ctiy, String addr, String lat, String lon) {
		super();
		this.ctiy = ctiy;
		this.addr = addr;
		this.lat = lat;
		this.lon = lon;
	}

	public MapInfo() {
		super();
	}

	public String getCtiy() {
		return ctiy;
	}

	public String getAddr() {
		return addr;
	}

	public String getLat() {
		return lat;
	}

	public String getLon() {
		return lon;
	}

	public void setCtiy(String ctiy) {
		this.ctiy = ctiy;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public void setLon(String lon) {
		this.lon = lon;
	}

	@Override
	public String toString() {
		return "MapInfo [ctiy=" + ctiy + ", addr=" + addr + ", lat=" + lat
				+ ", lon=" + lon + "]";
	}

}