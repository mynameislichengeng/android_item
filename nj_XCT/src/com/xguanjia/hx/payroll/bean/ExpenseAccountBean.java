package com.xguanjia.hx.payroll.bean;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;

public class ExpenseAccountBean implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String reimburseId = "";
	private String reimburseName;// 报销标题
	private String reimburseDate = "";
	private String createDate = "";
	private String location = "";
	private String expenseAmount;// 费用金额(用户消费)
	private String expenseContent = "";
	private String reimburseType;// 报销类型(1 交通,2住宿,3餐饮,4办公,5其他);
	private String typeState;// 类型状态(1已报销,2未报销)
	private String auditContent="";//审批意见
	private List<FilesBean> files=new ArrayList<FilesBean>();
	
	public String getReimburseId() {
		return reimburseId;
	}

	public void setReimburseId(String reimburseId) {
		this.reimburseId = reimburseId;
	}
	
	

	public String getAuditContent() {
		return auditContent;
	}

	public void setAuditContent(String auditContent) {
		this.auditContent = auditContent;
	}

	public String getReimburseDate() {
		return reimburseDate;
	}

	public void setReimburseDate(String reimburseDate) {
		this.reimburseDate = reimburseDate;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getExpenseContent() {
		return expenseContent;
	}

	public void setExpenseContent(String expenseContent) {
		this.expenseContent = expenseContent;
	}

	public List<FilesBean> getFiles() {
		return files;
	}

	public void setFiles(List<FilesBean> files) {
		this.files = files;
	}

	public String getReimburseName() {
		return reimburseName;
	}

	public void setReimburseName(String reimburseName) {
		this.reimburseName = reimburseName;
	}

	public String getExpenseAmount() {
		return expenseAmount;
	}

	public void setExpenseAmount(String expenseAmount) {
		this.expenseAmount = expenseAmount;
	}

	public String getReimburseType() {
		return reimburseType;
	}

	public void setReimburseType(String reimburseType) {
		this.reimburseType = reimburseType;
	}

	public String getTypeState() {
		return typeState;
	}

	public void setTypeState(String typeState) {
		this.typeState = typeState;
	}

}
