package com.xguanjia.hx.filecabinet.pojo;

import java.util.Date;
import java.util.List;

public class FolderBean {

	
	private int id = -1;              //文件ID
	private String folderName;        //文件名
	private String filePath;         //文件路径    本地存储路径(含文件名)
	private String fileUrl;          //文件远程存储路径
	private long fileSize;           //文件大小
	private int subFiles = 0;        //子文件个数
	private Date releaseDate;       //文件创建时间
	private int fileIcon = -1;       //文件图标
	private boolean isDirectory;      //判断是文件目录还是文件夹   true :文件夹  false:文件
	private List<FileBean> files;           //子文件列表
	private List<FolderBean> folders;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFolderName() {
		return folderName;
	}
	public void setFolderName(String folderName) {
		this.folderName = folderName;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public String getFileUrl() {
		return fileUrl;
	}
	public void setFileUrl(String fileUrl) {
		this.fileUrl = fileUrl;
	}
	public long getFileSize() {
		return fileSize;
	}
	public void setFileSize(long fileSize) {
		this.fileSize = fileSize;
	}
	public int getSubFiles() {
		return subFiles;
	}
	public void setSubFiles(int subFiles) {
		this.subFiles = subFiles;
	}
	public Date getReleaseDate() {
		return releaseDate;
	}
	public void setReleaseDate(Date releaseDate) {
		this.releaseDate = releaseDate;
	}
	public int getFileIcon() {
		return fileIcon;
	}
	public void setFileIcon(int fileIcon) {
		this.fileIcon = fileIcon;
	}
	public boolean isDirectory() {
		return isDirectory;
	}
	public void setDirectory(boolean isDirectory) {
		this.isDirectory = isDirectory;
	}
	public List<FileBean> getFiles() {
		return files;
	}
	public void setFiles(List<FileBean> files) {
		this.files = files;
	}
	public List<FolderBean> getFolders() {
		return folders;
	}
	public void setFolders(List<FolderBean> folders) {
		this.folders = folders;
	}
	
	
}
