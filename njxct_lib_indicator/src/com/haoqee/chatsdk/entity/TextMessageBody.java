package com.haoqee.chatsdk.entity;

import java.io.Serializable;

/**
 * 聊天文本消息
 *
 */
public class TextMessageBody extends BaseMessageBody implements Serializable{


	private static final long serialVersionUID = -11545454L;
	
	private String mContent = "";				//聊天文本内容

	public TextMessageBody(){
		
	}
	
	public TextMessageBody(String content){
		this.mContent = content;
	}
	
	//设置聊天文本
	public void setContent(String content){
		mContent = content;
	}
	
	//获取聊天文本
	public String getContent(){
		return mContent;
	}
}
